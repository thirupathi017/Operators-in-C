//assignment operator
#include<stdio.h>
int main()
{
    int a=10,b=20;
    //+=
    a+=b;
    printf("%d",a);
    //-=
    a-=b;
    printf("\n%d",a);
    //*=
    a*=b;
    printf("\n%d",a);
    // /=
    a/=b;
    printf("\n%d",a);
    // %=
    a%=b;
    printf("\n%d",a);
    return 0;
}