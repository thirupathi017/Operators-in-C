// relational operator
#include<stdio.h>
int main()
{
    int a=10,b=20;
    printf("equal                     :%d",a==b);
    printf("\ngreater than            :%d",a>b);
    printf("\nless than               :%d",a<b);
    printf("\ngreater than or equal to:%d",a>=b);
    printf("\nless than or equal to   :%d",a<=b);
    printf("\nnot equal to            :%d",a!=b);
    return 0;
}